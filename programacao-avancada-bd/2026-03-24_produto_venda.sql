CREATE OR REPLACE FUNCTION FUNC_CALCPRECOVENDA(P_PERC_MARGEM IN NUMBER)
RETURN NUMBER
IS
    V_NOVO_PRECO_VENDA PRODUTO_VENDA.PRECO_VENDA%TYPE;
BEGIN
    IF P_PERC_MARGEM>=20 AND P_PERC_MARGEM<=100 THEN
        FOR RS IN (SELECT ID, NVL(PRECO_VENDA, 0) PRECO_VENDA, PRECO_MEDIO_COMPRA
                    FROM PRODUTO_VENDA WHERE ATIVO = 'S')
                    --PRECO MEDIO,
        LOOP
            DBMS_OUTPUT.PUT_LINE('ID:'||RS.ID);
            DBMS_OUTPUT.PUT_LINE('PRECO_VENDA:'||RS.PRECO_VENDA);
            DBMS_OUTPUT.PUT_LINE('PRECO_MEDIO_COMPRA:'||RS.PRECO_MEDIO_COMPRA);
             DBMS_OUTPUT.PUT_LINE('P_PERC_MARGEM:'||P_PERC_MARGEM);
            DBMS_OUTPUT.PUT_LINE('MONO PRECO VENDA:'||(RS.PRECO_MEDIO_COMPRA+(RS.PRECO_MEDIO_COMPRA*(P_PERC_MARGEM/100))));
 
            V_NOVO_PRECO_VENDA := (RS.PRECO_MEDIO_COMPRA+(RS.PRECO_MEDIO_COMPRA*(P_PERC_MARGEM/100)));
 
            IF V_NOVO_PRECO_VENDA > RS.PRECO_VENDA THEN
                DBMS_OUTPUT.PUT_LINE('FAZER UPDATE');
                UPDATE PRODUTO_VENDA SET PRECO_VENDA = V_NOVO_PRECO_VENDA
                    WHERE  ID = RS.ID;
            END IF;
 
            DBMS_OUTPUT.PUT_LINE('-------------------------------------------------');
        END LOOP;
        COMMIT;
        RETURN 0;
    ELSE
        RETURN -1; --FORA DA FAIXA DO PERCENTUNTUAL DA MARGEM
    END IF;
 
EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        RETURN SQLCODE;
END;
