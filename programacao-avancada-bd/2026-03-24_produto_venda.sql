/*CRIAR UMA FUNÇÃO QUE AUMENTE O SALARIO DOS FUNCIONARIOS,
ESSA FUNÇÃO DEVE TER COMO PARAMETRO DE ENTRADA O INDICE DE AUMENTO,
QUE DEVE ESTAR ENTRE 0 (MAIOR QUE ZERO) E 15 PERCENTUAIS (MENOR IGUAL).
TODOS OS FUNCIONARIOS DEVE TER O AUMENTO, MENOS O PR4EIDENTE E VICE-PRSIDENTE.
O TETO MÁXIMO DE SALÁRIO DE UM FUNCIONARIO DEVE SER 10.000, MAS SE JÁ GANHA MAISQUE OS 10.000 DEVE
FICAR COM O SALARIO ATUAL (QUE É MAIOR QUE 10.000), ACRESCIDO DO PERCENTUAL DE AUMENTO.*/
 
CREATE OR REPLACE FUNCTION AUMENTA_SALARIO_AQB(P_PERC IN NUMBER)
RETURN NUMBER
IS
    V_RETORNO NUMBER(5);
    V_NOVO_SALARIO EMPLOYEES.SALARY%TYPE;
BEGIN
    IF P_PERC > 0 AND P_PERC <= 15 THEN
        FOR I IN (SELECT EMPLOYEE_ID, DEPARTMENT_ID, SALARY FROM FUNCIONARIOS_AQB WHERE JOB_ID != 'AD_PRES' AND JOB_ID != 'AD_VP')
        LOOP
            IF I.SALARY < 10000 THEN --AND I.DEPARTMENT_ID != ??? THEN --PROF DISSE P USAR O JOB_ID
                V_NOVO_SALARIO := (((P_PERC/100)*I.SALARY)+I.SALARY);
                UPDATE FUNCIONARIOS_AQB
                SET SALARY = V_NOVO_SALARIO
                WHERE EMPLOYEE_ID = I.EMPLOYEE_ID;
 
                V_RETORNO := 0; --SUCESSO
            ELSE
                V_RETORNO := -2; --SALARIO MAIOR OU IGUAL A 10000
            END IF;
        END LOOP;
    ELSE
        V_RETORNO := -1; --PERCENTUAL FORA DA FAIXA ACEITÁVEL        
    END IF;
    COMMIT;
    RETURN V_RETORNO;
EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        RETURN SQLCODE;
END;