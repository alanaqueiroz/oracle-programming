-- Passando procedure em function
CREATE OR REPLACE FUNCTION FUNC_AUMENTAR_SALARIO (
    P_ID          IN  PESSOA.ID%TYPE,
    P_PERCENTUAL  IN  NUMBER,
    P_SAIDA       OUT NUMBER
) RETURN NUMBER
IS
    V_SALARIO      PESSOA.SALARIO%TYPE;
    V_NOVO_SALARIO PESSOA.SALARIO%TYPE;
    V_RET          NUMBER(5);
BEGIN
    IF P_PERCENTUAL <= 0 OR P_PERCENTUAL >= 10 THEN
        V_RET := -998;
        P_SAIDA := V_RET;
        RETURN V_RET;
    END IF;

    BEGIN
        SELECT SALARIO
          INTO V_SALARIO
          FROM PESSOA
         WHERE ID = P_ID;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            V_RET := -999;
            P_SAIDA := V_RET;
            RETURN V_RET;
    END;

    V_NOVO_SALARIO := V_SALARIO + (V_SALARIO * P_PERCENTUAL / 100);

    IF V_SALARIO >= 10000 OR V_NOVO_SALARIO > 10000 THEN
        V_RET := -997;
        P_SAIDA := V_RET;
        RETURN V_RET;
    END IF;

    UPDATE PESSOA
       SET SALARIO = V_NOVO_SALARIO
     WHERE ID = P_ID;

    V_RET := 0;
    P_SAIDA := V_RET;

    RETURN V_RET;

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        V_RET := SQLCODE;
        P_SAIDA := V_RET;
        RETURN V_RET;
END;
/

