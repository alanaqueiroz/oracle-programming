CREATE OR REPLACE PROCEDURE PROC_AUMENTAR_SALARIO (
    P_ID         IN  PESSOA.ID%TYPE,
    P_PERCENTUAL IN  NUMBER,
    P_SAIDA      OUT NUMBER
)
IS
    V_SALARIO      PESSOA.SALARIO%TYPE;
    V_NOVO_SALARIO PESSOA.SALARIO%TYPE;
BEGIN
    IF P_PERCENTUAL <= 0 OR P_PERCENTUAL >= 10 THEN
        P_SAIDA := -998;
        RETURN;
    END IF;

    BEGIN
        SELECT SALARIO
          INTO V_SALARIO
          FROM PESSOA
         WHERE ID = P_ID;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            P_SAIDA := -999;
            RETURN;
    END;

    V_NOVO_SALARIO := V_SALARIO + (V_SALARIO * P_PERCENTUAL / 100);

    IF V_SALARIO >= 10000 OR V_NOVO_SALARIO > 10000 THEN
        P_SAIDA := -997;
        RETURN;
    END IF;

    UPDATE PESSOA
       SET SALARIO = V_NOVO_SALARIO
     WHERE ID = P_ID;

    P_SAIDA := 0;

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        P_SAIDA := SQLCODE;
END;
/

